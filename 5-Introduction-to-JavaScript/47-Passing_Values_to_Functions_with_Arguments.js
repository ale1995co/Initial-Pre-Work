// Parameterd are variables that act like placeholders for values to be input to a function when it is called.
// The actual values that are input into a function they are known as arguments.
// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
function functionWithArgs(a, b){
  console.log(a + b);
}
functionWithArgs(7, 9);
functionWithArgs(1, 2);
